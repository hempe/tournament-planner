CREATE TABLE IF NOT EXISTS social_events (
    id INT AUTO_INCREMENT PRIMARY KEY,
    tournamentId INT NULL,
    name VARCHAR(255) NOT NULL,
    date DATE NOT NULL,
    description TEXT NULL,
    registration_close DATETIME NULL,
    locked BOOLEAN NOT NULL DEFAULT 0,
    timestamp DATETIME NOT NULL,
    CONSTRAINT FK_SOCIAL_TOURNAMENT FOREIGN KEY (tournamentId) REFERENCES events(id) ON DELETE SET NULL
);

CREATE TABLE IF NOT EXISTS social_menus (
    id INT AUTO_INCREMENT PRIMARY KEY,
    socialEventId INT NOT NULL,
    name VARCHAR(255) NOT NULL,
    CONSTRAINT FK_MENU_SOCIAL FOREIGN KEY (socialEventId) REFERENCES social_events(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS social_tables (
    id INT AUTO_INCREMENT PRIMARY KEY,
    socialEventId INT NOT NULL,
    number INT NOT NULL,
    capacity INT NOT NULL,
    CONSTRAINT FK_TABLE_SOCIAL FOREIGN KEY (socialEventId) REFERENCES social_events(id) ON DELETE CASCADE
);

CREATE TABLE IF NOT EXISTS social_registrations (
    id INT AUTO_INCREMENT PRIMARY KEY,
    socialEventId INT NOT NULL,
    userId INT NULL,
    firstName VARCHAR(255) NULL,
    lastName VARCHAR(255) NULL,
    email VARCHAR(255) NULL,
    tableId INT NULL,
    menuId INT NULL,
    timestamp DATETIME NOT NULL DEFAULT CURRENT_TIMESTAMP,
    CONSTRAINT FK_SREG_EVENT FOREIGN KEY (socialEventId) REFERENCES social_events(id) ON DELETE CASCADE,
    CONSTRAINT FK_SREG_USER FOREIGN KEY (userId) REFERENCES users(id) ON DELETE CASCADE,
    CONSTRAINT FK_SREG_TABLE FOREIGN KEY (tableId) REFERENCES social_tables(id) ON DELETE SET NULL,
    CONSTRAINT FK_SREG_MENU FOREIGN KEY (menuId) REFERENCES social_menus(id)
